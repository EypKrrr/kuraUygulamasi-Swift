import UIKit

extension UIColor {
    
    static var ButtonBgColor: UIColor {
        return UIColor(named: "ButtonBgColor") ?? .white
    }
    
    static var ButtonTextColor: UIColor {
        return UIColor(named: "ButtonTextColor") ?? .white
    }
    
    static var OrderPageBgColor: UIColor {
        return UIColor(named: "OrderPageBgColor") ?? .white
    }
}
